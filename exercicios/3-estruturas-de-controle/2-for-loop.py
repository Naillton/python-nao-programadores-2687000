# Crie uma lista
cursos = ["Python para Iniciantes", "Data Science com Python", "Desenvolvimento Web com Django", "Machine Learning com R", "JavaScript Avançado"]

# Crie um for loop para imprimir cada elemento dessa lista
for curso in cursos:
  print(curso)

# Crie um objeto iterável utilizando a função range()
for i in range(6):
  # python inicia com o, entao i na string comeca com 1 ja que somamos + 1 a cada iteracao
  print(f"Estou aprendendo uma linguagem de programação. Iteração: {i + 1}")

# Use esse objeto iterável para criar um for loop que imprima na tela a frase "Estou aprendendo uma linguagem de programação."
for i in range(100):
  print(f"Estou aprendendo uma linguagem de programação.")